/**
 * Supported platforms.
 * @since 1.1.0
 */
export enum Platform {
    /**
     * macOS
     * @since 1.1.0
     */
    DARWIN = 'darwin',
    /**
     * Linux
     * @since 1.1.0
     */
    LINUX = 'linux',
    /**
     * Windows
     * @since 1.1.0
     */
    WIN32 = 'win32'
}

/**
 * Supported architectures.
 * @since 1.1.0
 */
export enum Architecture {
    /**
     * arm64
     * @since 1.1.0
     */
    ARM64 = 'arm64',
    /**
     * x64
     * @since 1.1.0
     */
    X64 = 'x64'
}